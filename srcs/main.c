#include "../includes/minishell.h"

int	main(void)
{
	char	*line;

	while (true)
	{
		line = readline("prompt> ");
		add_history(line);
		if (!ft_strncmp("quit", line, 4))
		{
			free(line);
			break ;
		}
	}
	return (EXIT_SUCCESS);
}
